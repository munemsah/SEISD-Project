  <h2 style="color:red;">Blood groups</h2>
  <p class="text-justify">There are four main blood groups (types of blood) – A, B, AB and O. Your blood group is determined by the genes you inherit from your parents. Each group can be either RhD positive or RhD negative, which means in total there are eight main blood groups.</p>

<h2 style="color:red;">The ABO system</h2>
<p class="text-justify"><p>
There are four main blood groups defined by the ABO system: <br>

blood group A – has A antigens on the red blood cells with anti-B antibodies in the plasma <br>
blood group B  – has B antigens with anti-A antibodies in the plasma <br>
blood group O  – has no antigens, but both anti-A and anti-B antibodies in the plasma <br>
blood group AB  – has both A and B antigens, but no antibodies</p>

<h2 style="color:red;"> The Rh system</h2>
<p class="text-justify">Red blood cells sometimes have another antigen, a protein known as the RhD antigen. If this is present, your blood group is RhD positive. If it's absent, your blood group is RhD negative. <br>

This means you can be one of eight blood groups:<br>

A RhD positive (A+)<br>
A RhD negative (A-)<br>
B RhD positive (B+)<br>
B RhD negative (B-)<br>
O RhD positive (O+)<br>
O RhD negative (O-)<br>
AB RhD positive (AB+)<br>
AB RhD negative (AB-)<br>
About 85% of the UK population is RhD positive (36% of the population has O+, the most common type).

In most cases, O RhD negative blood (O-) can safely be given to anyone. It's often used in medical emergencies when the blood type isn't immediately known.

It's safe for most recipients because it doesn't have any A, B or RhD antigens on the surface of the cells, and is compatible with every other ABO and RhD blood group.

The NHS Blood and Transplant (NHSBT) website has more information about the RH system.</p>



<h2 style="color:red;">Giving blood</h2>
<p class="text-justify">
Most people are able to give blood, but only 4% actually do. You can donate blood if you:<br>

are fit and healthy<br>
weigh at least 50kg (7st 12lb)<br>
are 17-66 years old (or 70 if you've given blood before)<br>
are over 70 and have given blood in the last two years</p>
